package com.qfedu.pojo;

/**
 * 优惠劵对应类
 */
public class Coupons1 {
	private int cou_id;
	private String uuid;
	private Double cou_money;
	private String cou_time;

	public int getCou_id() {
		return cou_id;
	}

	public void setCou_id(int cou_id) {
		this.cou_id = cou_id;
	}

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

	public Double getCou_money() {
		return cou_money;
	}

	public void setCou_money(Double cou_money) {
		this.cou_money = cou_money;
	}

	public String getCou_time() {
		return cou_time;
	}

	public void setCou_time(String cou_time) {
		this.cou_time = cou_time;
	}

}
